# 14. The radius of the circle has the value 5.
# Write a program that calculates the area and circumference of the circle.
# Use the algorithm below.

#####
# Calculation of the area and circumference of a circle
##

# determine radius and PI
r = 5
PI = 3.14

# calculate area 
area = PI*r**2

# calculate circumference 
circumference = 2*PI*r

# display results 
print(f"r = {r}\nPI = {PI}\narea = {area}\ncircumference = {circumference}")

